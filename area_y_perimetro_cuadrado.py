# 1 - Solicitar la longitud del lado
lado = float(input("Por favor, introduce la longitud de un lado del cuadrado en cm: "))

# 2 - Calcular el área y el perímetro
area = lado * 2
perimetro = lado * 4

# 3 - Mostrar el resultado
print(f"El resultado del área del cuadrado es: {area}")
print(f"El resultado del perímetro del cuadrado es: {perimetro}")
